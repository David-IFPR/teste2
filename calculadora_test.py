# Valores definidos
a = 11
b = 0  # Teste com zero

# Operações
soma = a + b
subtracao = a - b
multiplicacao = a * b

# Verificação da divisão
if b != 0:
    divisao = a / b
else:
    divisao = "Erro: divisão por zero"

# Exibindo os resultados
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão:", divisao)